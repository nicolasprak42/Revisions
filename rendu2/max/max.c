#include <unistd.h>

int max(int *tab, unsigned int len)
{
	int	max;
	int i;

	max = tab[0];
	i = 0;
	while (i < len)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
